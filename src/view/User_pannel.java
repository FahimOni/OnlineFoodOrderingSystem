    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        if (beefrad.isSelected() == true) {
            item = "Beef Burger";
            price = 100;
        } else if (chickenrad.isSelected() == true) {
            item = "Chicken Burger";
            price = 90;
        } else if (veggierad.isSelected() == true) {
            item = "Pasta";
            price = 250;
        } else if (turkeyrad.isSelected() == true) {
            item = "Special Pizza";
            price = 500;
        }

        int itemprice = 0;
        int quantity = Integer.parseInt(quantitytxt.getValue().toString());
        //nt itemprice;
        if (quantity <= 0) {
            JOptionPane.showMessageDialog(this, "Please select a valid quantity");
        } else if (eggck.isSelected() || cheeseck.isSelected() || extrack.isSelected()) {
            //itemprice= quantity * (price+20);
            if (eggck.isSelected()) {
                itemprice = quantity * (price + 15);
            }
            if (cheeseck.isSelected()) {
                itemprice = quantity * (price + 20);
            }
            if (extrack.isSelected()) {
                itemprice = quantity * (price + 60);
            }
            if (extrack.isSelected() && cheeseck.isSelected()) {
                itemprice = quantity * (price + 80);
            }
            if (extrack.isSelected() && eggck.isSelected()) {
                itemprice = quantity * (price + 75);
            }
            if (cheeseck.isSelected() && eggck.isSelected()) {
                itemprice = quantity * (price + 35);
            }

        } else {
            itemprice = quantity * price;
        }
        model = (DefaultTableModel) jTable1.getModel();

        if (quantity > 0) {
            model.addRow(new Object[]{
                item,
                price,
                quantity,
                itemprice

            });
        }

        int sum = 0;
        for (int a = 0; a < jTable1.getRowCount(); a++) {
            sum = sum + Integer.parseInt(jTable1.getValueAt(a, 3).toString());

        }
        totaltxt.setText(Integer.toString(sum));


    }//GEN-LAST:event_jButton6ActionPerformed

