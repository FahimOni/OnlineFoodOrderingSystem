            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection(url, user, password);

            String total = totaltxt.getText();
            String balance = balancetxt.getText();
            String payment = paymenttxt.getText();

            String query = "insert into sales (id,subtotal,pay,bal)values('"+x+"',?,?,?)";
            pst = con.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, total);
            pst.setString(2, payment);
            pst.setString(3, balance);
            pst.executeUpdate();

